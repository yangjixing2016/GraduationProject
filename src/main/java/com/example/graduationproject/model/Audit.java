package com.example.graduationproject.model;

public class Audit {
    private int auditId;
    private String auditName;
    private String auditSex;
    private int auditAge;
    private String auditGraduationSchool;
    private int auditBrowseCount;
    private int auditAdoptCount;
    private int auditWorkYear;
    private String auditPortrait;
    private String auditType;

    public String getAuditType() {
        return auditType;
    }

    public void setAuditType(String auditType) {
        this.auditType = auditType;
    }

    public int getAuditId() {
        return auditId;
    }

    public void setAuditId(int auditId) {
        this.auditId = auditId;
    }

    public String getAuditName() {
        return auditName;
    }

    public void setAuditName(String auditName) {
        this.auditName = auditName;
    }

    public String getAuditSex() {
        return auditSex;
    }

    public void setAuditSex(String auditSex) {
        this.auditSex = auditSex;
    }

    public int getAuditAge() {
        return auditAge;
    }

    public void setAuditAge(int auditAge) {
        this.auditAge = auditAge;
    }

    public String getAuditGraduationSchool() {
        return auditGraduationSchool;
    }

    public void setAuditGraduationSchool(String auditGraduationSchool) {
        this.auditGraduationSchool = auditGraduationSchool;
    }

    public int getAuditBrowseCount() {
        return auditBrowseCount;
    }

    public void setAuditBrowseCount(int auditBrowseCount) {
        this.auditBrowseCount = auditBrowseCount;
    }

    public int getAuditAdoptCount() {
        return auditAdoptCount;
    }

    public void setAuditAdoptCount(int auditAdoptCount) {
        this.auditAdoptCount = auditAdoptCount;
    }

    public int getAuditWorkYear() {
        return auditWorkYear;
    }

    public void setAuditWorkYear(int auditWorkYear) {
        this.auditWorkYear = auditWorkYear;
    }

    public String getAuditPortrait() {
        return auditPortrait;
    }

    public void setAuditPortrait(String auditPortrait) {
        this.auditPortrait = auditPortrait;
    }
}
