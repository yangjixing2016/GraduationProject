package com.example.graduationproject.model;

public class DraftFree {
    private int draftFreeId;
    private int draftInformationId;
    private int draftFreeMoney;
    private String draftFreeDate;
    private String draftFreeStuta;

    public int getDraftFreeId() {
        return draftFreeId;
    }

    public void setDraftFreeId(int draftFreeId) {
        this.draftFreeId = draftFreeId;
    }

    public int getDraftInformationId() {
        return draftInformationId;
    }

    public void setDraftInformationId(int draftInformationId) {
        this.draftInformationId = draftInformationId;
    }

    public int getDraftFreeMoney() {
        return draftFreeMoney;
    }

    public void setDraftFreeMoney(int draftFreeMoney) {
        this.draftFreeMoney = draftFreeMoney;
    }

    public String getDraftFreeDate() {
        return draftFreeDate;
    }

    public void setDraftFreeDate(String draftFreeDate) {
        this.draftFreeDate = draftFreeDate;
    }

    public String getDraftFreeStuta() {
        return draftFreeStuta;
    }

    public void setDraftFreeStuta(String draftFreeStuta) {
        this.draftFreeStuta = draftFreeStuta;
    }
}
